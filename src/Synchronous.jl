  pitch::AbstractLength 
  """Belt linear length if cut"""
  length::AbstractLength 
  """Number of teeth on the belt"""
  nTeeth::Int 
  """Width of the belt"""
  width::AbstractLength 
  """Reference part number"""
  partNumber::String 
  """Reference supplier"""
  supplier::String 
  """Reference sourcing url"""
  url::String 
  """Autogenerated unique id"""
  id::UUIDs.UUID
end 

